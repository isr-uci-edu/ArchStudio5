package org.archstudio.myx.fw;

public class MyxUnsupportedBrickDescriptionException extends Exception {

	public MyxUnsupportedBrickDescriptionException() {
		super();
	}

	public MyxUnsupportedBrickDescriptionException(String message) {
		super(message);
	}

	public MyxUnsupportedBrickDescriptionException(Throwable cause) {
		super(cause);
	}

	public MyxUnsupportedBrickDescriptionException(String message, Throwable cause) {
		super(message, cause);
	}

}
